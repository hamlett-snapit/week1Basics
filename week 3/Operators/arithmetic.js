// Arithmetic Operators 

// Operands , are simply variables.
var x = 149;
console.log("The value of x is : " + x);
var y = 260;
console.log("The value of y is : " + y);
var result;

// Addition 
result = x + y;
console.log("Addition : " +result);

// Subtraction 
result = x - y;
console.log("Subtraction : " + result);

// Multiplication
result = x * y;
console.log("Mulitiplication : " + result);

// Division
result = x / y;
console.log("Division : " + result);

// Remainder 
result = y % x;
console.log(" Remainder : " + result);

// Increment && Decrement 
result = ++x;
console.log(result)
result = --y;
console.log(result)

