// Relational Operators 

// Operands
var x = 80;
var y = 46; 
var result;

// Equals to
result = x == y;
console.log(result);

// Not Equals 
result = x != y;
console.log(result);

// Greater Than
result = x > y;
console.log(result);

// Less Than
result = x < y;
console.log(result);

// Greater Than Or Equal to
result = x >= y;
console.log(result);

// Less Than Or Equal to 
result = x <= y;
console.log(result);